def tokenizer(text):
    text = text.lower()
    text = re.sub(r'<[^>]+>', ' ', text)
    text = re.sub(r'[^a-z\s]', '', text)
    tokens = text.split()
    tokens = [token for token in tokens if 2 < len(token) < 20]
    return [stemmer.stem(token) for token in tokens if token not in stopwords]

