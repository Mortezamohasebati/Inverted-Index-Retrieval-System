def plot_term_overlap_matrix(methods):
    names = list(methods.keys())
    term_sets = {name: set(index.keys()) for name, index in methods.items()}

    overlap_matrix = pd.DataFrame(index=names, columns=names)

    for i in names:
        for j in names:
            if i == j:
                overlap_matrix.loc[i, j] = len(term_sets[i])
            else:
                overlap_matrix.loc[i, j] = len(term_sets[i].intersection(term_sets[j]))

    overlap_matrix = overlap_matrix.astype(int)

    plt.figure(figsize=(8, 6))
    sns.heatmap(overlap_matrix, annot=True, fmt='d', cmap='Blues', cbar=True)
    plt.title("Term Overlap Between Methods", fontsize=14)
    plt.xlabel("Method")
    plt.ylabel("Method")
    plt.tight_layout()
    plt.show()

