def extract_articles_with_topics(file_path):
    with open(file_path, 'r', encoding='latin-1') as file:
        data = file.read()
        data = data.replace('&#2;', '').replace('&#3;', '')
        soup = BeautifulSoup(data, 'html.parser')
        all_articles = soup.find_all('reuters')
        articles_yes = [a for a in all_articles if a.get('topics') == 'YES']
        return articles_yes, len(all_articles)

