class PostingList:
    def __init__(self):
        self.data_list = []
        self.doc_ids = set()
        self.total_tf = 0

    def add(self, doc_id, tf=1):
        for i, (d_id, count) in enumerate(self.data_list):
            if d_id == doc_id:
                self.data_list[i] = (d_id, count + tf)
                self.total_tf += tf
                return
        self.data_list.append((doc_id, tf))
        self.doc_ids.add(doc_id)
        self.total_tf += tf

    def __repr__(self):
        return (
            f"docs: {sorted(self.doc_ids)} , "
            f"DF: {len(self.doc_ids)} , "
            f"TTF: {self.total_tf} , "
            f"data: {self.data_list}"
        )

