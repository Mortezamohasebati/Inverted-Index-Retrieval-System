def data_parser(article):
    newid = int(article['newid'])
    topics_tag = article.find('topics')
    title_tag = article.find('title')
    body_tag = article.find('body')

    topics = [d.get_text() for d in topics_tag.find_all('d')] if topics_tag else []
    title = title_tag.get_text() if title_tag else ""
    body = body_tag.get_text() if body_tag else ""

    return newid, topics, title, body

