def tokenizer_by_dict(text):
    text = text.lower()
    text = re.sub(r'<[^>]+>', ' ', text)
    text = re.sub(r'[^a-z\s]', '', text)
    tokens = text.split()
    tokens = [token for token in tokens if 2 < len(token) < 20]
    valid_tokens = [token for token in tokens if is_valid_word(token)]
    return [stemmer.stem(token) for token in valid_tokens if token not in stopwords]

